# Write a script to get the maximum and minimum VALUE in a dictionary

test_dict = {
  "one": 4675,
  667: 5560,
  "car": 332,
  "elephant": 9985,
  17: 6653,
  True: 556,
  (1.2, 1.4, 1.5): 0
}

